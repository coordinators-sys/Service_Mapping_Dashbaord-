function t(key, params) {
  const dict = TRANSLATIONS[CURRENT_LANG] || TRANSLATIONS.en;
  let text = dict[key] ?? TRANSLATIONS.en[key] ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.split(`{${k}}`).join(v);
    });
  }
  return text;
}

function applyTranslations(lang) {
  CURRENT_LANG = TRANSLATIONS[lang] ? lang : "en";
  const dict = TRANSLATIONS[CURRENT_LANG];
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] || TRANSLATIONS.en[key]) el.textContent = t(key);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key] || TRANSLATIONS.en[key]) el.setAttribute("placeholder", t(key));
  });
  document.documentElement.setAttribute("lang", CURRENT_LANG);
}

function currentLang() {
  return CURRENT_LANG;
}
